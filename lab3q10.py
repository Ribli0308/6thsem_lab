'''10. wap to demonstrate the predefined string functions.'''
string = 'devashree'
print("The string is ", string)
print(string.capitalize())
print("Count e: ", string.count('e'))
print("Does the string end with d? ", string.endswith('d'))
print("Find position r: ", string.find('r'))
print("Is alphabet? ", string.isalpha())
print("Is digit? ", string.isdigit())
print("Is upper? ", string.isupper())
print("Is lower? ",string.islower())
print("upper case: ", string.upper())
print("Replace: ", string.replace('shree', 'deva'))